package com.jd.platform.jlog.core;

public class Server{
        private int port;
        private String addr;
        private ObjObj objObj;

        public int getPort() {
            return port;
        }

        public void setPort(int port) {
            this.port = port;
        }

        public String getAddr() {
            return addr;
        }

        public void setAddr(String addr) {
            this.addr = addr;
        }


    public ObjObj getObjObj() {
        return objObj;
    }

    public void setObjObj(ObjObj objObj) {
        this.objObj = objObj;
    }
}